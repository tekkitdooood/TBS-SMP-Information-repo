function crushing_wheel (input, output, event) {
    event.custom({
        "type": "create:crushing",
        "ingredients": [
            {"tag": input}
            ],
        "processing_time": 250,
        "results": output
        }
    )
}

function crusher (input, output, event) {
    event.custom({
        "type": "immersiveengineering:crusher",
        "energy": 3200,
        "input": {"tag": input},
        "result": {
            "basePredicate": {
                "item": output
            },
            "count": 2
            } 
        }
    )
}

function grinder (input, output, byproduct, event) {
    event.custom({
        "type": "electrodynamics:mineral_grinder_recipe",
        "experience": 0.2,
        "group": "electrodynamics",
        "itembi": byproduct,
        "iteminputs": [
            {
            "count": 1,
            "ingredient": {
                "tag": input
                }
            }
        ],
        "output": {
            "count": 3,
            "id": output
            },
        "ticks": 140,
        "usagepertick": 700.0
        }
    )
}